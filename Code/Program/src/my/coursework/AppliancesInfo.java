    private void miImportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_miImportActionPerformed
        try {
            File selectedFile = new File("D:\\data.csv");
            if(selectedFile.exists()) {
                if(selectedFile.length()==0) {
                    JOptionPane.showMessageDialog(rootPane,"There is No Previously Saved Data" ,"Error!!",JOptionPane.ERROR_MESSAGE); 
                }
                else{
                    BufferedReader br = new BufferedReader(new FileReader(selectedFile));
                    Object[] lines = br.lines().toArray();
                    int rowCount = tblInfo.getRowCount();
                    int nextRow = 0;
                    boolean emptyRowFlag = false;
                    String p;

                    for (int i = 0; i < lines.length; i++) {  
                        String[] row = lines[i].toString().split(",");
                        do {
                            p = (String) tblInfo.getValueAt(nextRow, 0);
                    
                            if (p != null && p.length() != 0) {
                                nextRow++;
                            } 
                    
                            else {
                                emptyRowFlag = true;
                            }
                        }
                        while (nextRow < rowCount && !emptyRowFlag);

                        if (emptyRowFlag) {
                            int colCount = tblInfo.getColumnCount();
                    
                            if (nextRow < rowCount) {

                                for (int j = 0; j < colCount; j++) {
                                    tblInfo.setValueAt(row[j], nextRow, j);
                                }
                            }
                        } 
                        else {
                            JOptionPane.showMessageDialog(rootPane, "No Empty Row Found!!", "Error!!", JOptionPane.ERROR_MESSAGE);
                        }
                    }
                }
            } 
            else{
                selectedFile.createNewFile();
                JOptionPane.showMessageDialog(rootPane,"There is No Previously Saved Data" ,"Error!!",JOptionPane.ERROR_MESSAGE); 
                return;
            }
        }
        
        catch (Exception ex) {
            Logger.getLogger(AppliancesInfo.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_miImportActionPerformed

