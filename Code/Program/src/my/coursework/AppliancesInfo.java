    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        try {
            String modelNumber = txtModelNumber.getText().trim();
            String name = txtName.getText().trim();
            String category = (String)cbCategory.getSelectedItem();
            String recommendationType;
            
            //selecting recommendation from radio button
            if(rbtnLow.isSelected() == true){
                recommendationType = "Low"; 
            }
            else if(rbtnMedium.isSelected() == true){
                recommendationType = "Medium";
            }
            else {
                recommendationType = "High";
            }
            String recommendation = recommendationType;
            
            int price = Integer.parseInt(txtPrice.getText().trim());
            String discount = (String)cbDiscount.getSelectedItem();
            boolean emptyRowFlag = false;
            
            if (!modelNumber.equals("Enter Model Number") && !name.equals("Enter Name") && !recommendation.equals("Enter Recommendation") && !discount.equals("Enter Discount") && price > 0) {
                String[] value = {modelNumber, name, category, recommendation, Integer.toString(price), discount};
                int rowCount = tblInfo.getRowCount();
                int nextRow = 0;
                String p;
                
                do {
                    p = (String) tblInfo.getValueAt(nextRow, 0);
                    if (p != null && p.length() != 0) {
                        nextRow++;
                    } 
                    
                    else {
                        emptyRowFlag = true;
                    }
                } 
                while (nextRow < rowCount && !emptyRowFlag);
                
                if (Item.size() >= 1) {
                    
                    for (EverestComputers n : Item) {
                        
                        if (modelNumber.equals(n.getModelNumber())) {
                            emptyRowFlag = false;
                            JOptionPane.showMessageDialog(rootPane, "Model Number Must Not Be Same!!", "Error!!", JOptionPane.ERROR_MESSAGE);
                            return  ;
                        } 
                        
                        else {
                            emptyRowFlag = true;
                        }
                    }
                }
                else{
                    emptyRowFlag = true;
                }
                
                if (emptyRowFlag) {
                    int colCount = tblInfo.getColumnCount();
                    
                    if (nextRow < rowCount) {
                        
                        EverestComputers Object = new EverestComputers(modelNumber, name, category, recommendation, price, discount);
                        Item.add(Object);
                        mergeSort(Item);
                        
                        for (int i = 0; i < colCount; i++) {
                            tblInfo.setValueAt(value[i], nextRow, i);
                        }
                    } 
                    else {
                        JOptionPane.showMessageDialog(rootPane, "No Empty Row Found!!", "Error!!", JOptionPane.ERROR_MESSAGE);
                    }
                    save(); //calls save() method for saving the data to created file
                } 
            }
            else if(price < 1){
                JOptionPane.showMessageDialog(rootPane,"Input Value Not Valid For Price." ,"Invalid Input!!", JOptionPane.ERROR_MESSAGE);
            }
            else {
                JOptionPane.showMessageDialog(rootPane, "Do Not Leave The Textfields Empty", "Error!!", JOptionPane.ERROR_MESSAGE);
            }
        } 
        catch (NullPointerException npe) {
            JOptionPane.showMessageDialog(rootPane, "No Data, Check Your Input", "Error!!", JOptionPane.ERROR_MESSAGE);
        }
        catch (NumberFormatException nfe) {
            JOptionPane.showMessageDialog(rootPane, "Error While Retrieving Data, Check Your Input", "Error!!", JOptionPane.ERROR_MESSAGE);
        }   
    }//GEN-LAST:event_btnAddActionPerformed
    
    private void save() {
        File file = new File("D:\\data.csv");//Loading of file
        
        try (FileWriter wr = new FileWriter(file)) {
            
            if (file.isFile()) {
                file.createNewFile();
            }    
            
            for (EverestComputers s : Item) {
                String v[] = {s.getModelNumber(), s.getName(), s.getCategory(), s.getRecommendation(), Integer.toString(s.getPrice()), s.getDiscount()};
                
                for (int i = 0; i < v.length; i++) {
                    wr.append(v[i]);
                    if (i == v.length - 1) {
                        break;
                    }
                    wr.append(",");//CSV comma separated value
                }
                wr.append("\n");
            }
            wr.flush();
        }
        catch (IOException ex) {
            Logger.getLogger(AppliancesInfo.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
        
