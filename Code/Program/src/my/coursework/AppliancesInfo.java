    public void mergeSort(ArrayList<EverestComputers> price) {
        if (price.size() <= 1) {
            return;
        }
        
        int firstLength = price.size() / 2;
        int secondLength = price.size() - firstLength;
        ArrayList<EverestComputers> first = new ArrayList();
        ArrayList<EverestComputers> second = new ArrayList();
        
        for (int i = 0; i < firstLength; i++) {
            first.add(price.get(i));
        }

        for (int i = 0; i < secondLength; i++) {
            second.add(price.get(firstLength + i));
        }
        
        mergeSort(first);
        mergeSort(second);
        merge(first, second, price);
    }

    public static void merge(ArrayList<EverestComputers> first, ArrayList<EverestComputers> second, ArrayList<EverestComputers> price) {
        int iFirst = 0;
        int iSecond = 0;
        int j = 0;
        
        while (iFirst < first.size() && iSecond < second.size()) {
            if (first.get(iFirst).getPrice() < second.get(iSecond).getPrice()) {
                price.set(j, first.get(iFirst));
                iFirst++;
            }
            else {
                price.set(j, second.get(iSecond));
                iSecond++;
            }
            j++;
        }
        
        while (iFirst < first.size()) {
            price.set(j, first.get(iFirst));
            iFirst++;
            j++;
        }

        while (iSecond < second.size()) {
            price.set(j, second.get(iSecond));
            iSecond++;
            j++;
        }
    }
    
    public static int binarySearch(ArrayList<EverestComputers> priceSearch, int low, int high, int key) {  
        int mid = (low + high)/2;  
        if (low <= high){ 
            
            if ( priceSearch.get(mid).getPrice() == key ){  
                return mid;     
            }
            
            else if ( priceSearch.get(mid).getPrice() > key ){  
                return binarySearch(priceSearch, low, mid-1, key);
            }
            
            else{  
                return binarySearch(priceSearch, mid+1, high, key); 
            }  
        }  
        
        else { 
            return -1;
        }  
    }  
        
